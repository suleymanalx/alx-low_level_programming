/**
 * print_alphabet -  Function
 * void - print_alphabet function does not accepts a parameter
 *
 * Description: prints out characters (a-z) on consule using write
 * Return: print_alphabet does have a return value
 */
void print_alphabet(void)
{
char i;
for (i = 'a'; i <= 'z'; i++)
{
_putchar(i);
}
_putchar('\n');
}